//! Generates the zerocode Quickstart keybinding snippet for the mdBook
//! from the canonical default keymap. Run with `cargo run -p xtask --bin
//! keymap-doc`; writes `docs/book/src/_snippets/zerocode-quickstart-keys.md`.
//!
//! The snippet is a committed artifact: mdBook has no codegen preprocessor,
//! so the table is regenerated here and checked in. Re-run after changing
//! any default chord in `apps/zerocode/src/keymap/actions.rs`.

use std::fmt::Write as _;
use std::path::PathBuf;

use zerocode::config::keybindings::{DEFAULT_PRESET_NAME, preset_names};
use zerocode::keymap::{Chord, GlobalAction, QuickstartTabAction, RebindableActions};

fn render_chords(chords: &[Chord]) -> String {
    let mut seen: Vec<String> = Vec::new();
    for c in chords {
        let d = c.display();
        if !seen.contains(&d) {
            seen.push(d);
        }
    }
    seen.join(" / ")
}

fn quickstart_rows() -> Vec<(String, &'static str)> {
    QuickstartTabAction::all()
        .iter()
        .map(|a| (render_chords(&a.defaults()), a.human_label()))
        .collect()
}

fn pane_nav_rows() -> Vec<(String, &'static str)> {
    GlobalAction::all()
        .iter()
        .filter(|a| matches!(a, GlobalAction::PaneNavLeft | GlobalAction::PaneNavRight))
        .map(|a| (render_chords(&a.defaults()), a.human_label()))
        .collect()
}

fn main() -> anyhow::Result<()> {
    let mut out = String::new();
    out.push_str(
        "<!--\n  GENERATED by `cargo run -p xtask --bin keymap-doc`.\n  \
         Do not edit by hand. Source: apps/zerocode/src/keymap/actions.rs\n  \
         (the default preset). Re-run after changing any default chord.\n-->\n\n",
    );

    out.push_str("Switch to the **Quickstart** pane:\n\n");
    out.push_str("| Keys | Action |\n|---|---|\n");
    for (keys, label) in pane_nav_rows() {
        writeln!(out, "| `{keys}` | {label} |")?;
    }

    out.push_str("\nInside the pane:\n\n");
    out.push_str("| Keys | Action |\n|---|---|\n");
    for (keys, label) in quickstart_rows() {
        writeln!(out, "| `{keys}` | {label} |")?;
    }

    let others: Vec<&str> = preset_names()
        .filter(|n| *n != DEFAULT_PRESET_NAME)
        .collect();
    writeln!(
        out,
        "\nThese are the `{DEFAULT_PRESET_NAME}` preset. You can switch \
         presets ({}) in the zerocode Keybindings tab, or rebind any \
         action individually.",
        others.join(", ")
    )?;

    let dest = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("../docs/book/src/_snippets/zerocode-quickstart-keys.md");
    std::fs::write(&dest, out)?;
    println!("wrote {}", dest.display());
    Ok(())
}
